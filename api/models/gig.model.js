import mongoose from 'mongoose';
const { Schema } = mongoose;

const gigSchema = new Schema({
    userId:
    {
        type:String,
        required,
    },
     title:
    {
        type:String,
        required,
    },
     description:
    {
        type:String,
        required,
    },
     totalStars:
    {
        type:Number,
        required,
    },
     starNumber:
    {
        type:Number,
        required,
    },
     cat:
    {
        type:String,
        required,
    }, 
    price:
    {
        type:Number,
        required,
    },
    cover:
    {
        type:String,
        required,
    },
    images:
    {
        type:String,
        required,
    },
    shortTime:
    {
        type:String,
        required,
    },
    shortDesc:
    {
        type:String,
        required,
    },
     deliveryTime:
    {
        type:String,
        required,
    },
     userId:
    {
        type:String,
        required,
    },
},
{
   timestamps: true
});
export default mongoose.model("Gig", gigSchema);